package introduction

fun main() {
    // (+ * - /)
    var age = 31
    age = age + 10
    println(age)

    var myAge = "31"
    println(myAge + " my age") // concatenation

    // (!)
    var boolean = false
    // true or false (1 or 0)

    // "!" means negation (inverts the logic)
    println(!boolean)

}