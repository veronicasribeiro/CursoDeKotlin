package introduction

// \n - Skip a line
fun main() {
    var name = "Verônica"
    var surname = "Ribeiro"
    var skipALine = '\n'
    println(name + skipALine + surname)

    println("-------------------")

    // \t - Make a Tab
    var firstName = "Verônica"
    var lastName = "Ribeiro"
    var makeATab = '\t'
    println(firstName + makeATab + lastName)

}

