package introduction

// \n - Skip a line
fun main() {
    val name = "Verônica"
    val surname = "Ribeiro"
    val skipALine = '\n'
    println(name + skipALine + surname)

    println("-------------------")

    // \t - Make a Tab
    val firstName = "Verônica"
    val lastName = "Ribeiro"
    val makeATab = '\t'
    println(firstName + makeATab + lastName)

}

